package link.smartwall.controls.webview.support;


/**
 * 事件监听info
 * Created by liupei on 2017/4/17.
 */

public class JSEventInfo {
    private String eventName; //事件名
    private String fnid;

    public String getEventName() {
        return eventName;
    }

    public void setEventName(String eventName) {
        this.eventName = eventName;
    }

    public String getFnid() {
        return fnid;
    }

    public void setFnid(String fnid) {
        this.fnid = fnid;
    }
}
